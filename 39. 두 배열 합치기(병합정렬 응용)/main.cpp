#include<stdio.h>
#include<iostream>
#include<vector>
using namespace std; 

/*

<39. 두 배열 합치기(병합정렬 응용)>

오름차순으로 정렬이 된 두 배열이 주어지면 
두 배열을 오름차순으로 합쳐 출력하는 프로그램을 작성하세요.

=============================================================================

▶ 입력설명
첫 번째 줄에 첫 번째 배열의 크기 N(1 <= N <= 100)이 주어집니다.
두 번째 줄에 N개의 배열 원소가 오름차순으로 주어집니다.
세 번째 줄에 두 번째 배열의 크기 M(1 <= M <= 100)이 주어집니다.
네 번째 줄에 M개의 배열 원소가 오름차순으로 주어집니다.
각 배열의 원소는 int형 변수의 크기를 넘지 않습니다.

▶ 출력설명
오름차순으로 정렬된 배열을 출력합니다.

=============================================================================

▶ 입력예제
3
1 3 5
5
2 3 6 7 9

▶ 출력예제
1 2 3 3 5 6 7 9

*/

int main()
{
	// 최초의 풀이 (강의 풀이와 동일) 

	int n, m, p1{ 0 }, p2{ 0 }, p3{ 0 };
	int arr1[100]{ 0 }, arr2[100]{ 0 }, res[200]{ 0 };

	cin >> n;

	for (int i = 0; i < n; i++)
	{
		cin >> arr1[i];
	}

	cin >> m; 

	for (int i = 0; i < m; i++)
	{
		cin >> arr2[i];
	}

	// 오름차순 정렬 

	while (p1 < n && p2 < m)
	{
		if (arr1[p1] < arr2[p2])
		{
			res[p3++] = arr1[p1++];
		}
		else
		{
			res[p3++] = arr2[p2++];
		}
	}

	while (p1 < n) res[p3++] = arr1[p1++];
	while (p2 < m) res[p3++] = arr2[p2++];

	for (int i = 0; i < n + m; i++)
	{
		cout << res[i] << " ";
	}

	return 0;
}